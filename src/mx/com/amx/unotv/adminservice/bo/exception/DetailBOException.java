public class DetailBOException extends Exception {

	
	private static final long serialVersionUID = 1L;

	public DetailBOException() {
		super();
	}

	
	public DetailBOException(String message, Throwable cause) {
		super(message, cause);
	}

	
	public DetailBOException(String message) {
		super(message);
	}

	public DetailBOException(Throwable cause) {
		super(cause);
	}


}
