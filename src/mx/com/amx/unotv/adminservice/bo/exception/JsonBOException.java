package mx.com.amx.unotv.adminservice.bo.exception;

public class JsonBOException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public JsonBOException() {
		super();
	}

	public JsonBOException(String message, Throwable cause) {
		super(message, cause);
	}

	public JsonBOException(String message) {
		super(message);
	}

	public JsonBOException(Throwable cause) {
		super(cause);
	}
	
}
